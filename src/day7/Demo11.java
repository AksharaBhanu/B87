package day7;

public class Demo11 {

	public static void main(String[] args) {
		int a=10;
		System.out.println(a++);//10
		System.out.println(a);//11
		
		int b=10;
		System.out.println(++b);//11
		System.out.println(b);//11
		
		int c=10;
		System.out.println(c--);//10
		System.out.println(c);//9
		
		int d=10;
		System.out.println(--d);//9
		System.out.println(d);//9
	}

}
